package simple.number;

public class Operations {
  int addition(int x, int y) {
    return x + y;
  }

  int division(int x, int y) {
    return x / y;
  }

  int modulo(int x, int y) {
    return x % y;
  }

  int multiplication(int x, int y) {
    return x * y;
  }

  int subtraction(int x, int y) {
    return x - y;
  }
}
